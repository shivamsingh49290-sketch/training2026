#olympic symbol
import turtle
t=turtle.Turtle()
t.pensize(10)
t.pencolor('blue')
t.circle(40)
t.pu()
t.goto(50,-40)
t.pd()
t.pencolor('yellow')
t.circle(40)
t.pu()
t.goto(100,0)
t.pd()
t.pencolor('black')
t.circle(40)
t.pu()
t.goto(150,-40)
t.pd()
t.pencolor('green')
t.circle(40)
t.pu()
t.goto(200,0)
t.pd()
t.pencolor('red')
t.circle(40)
t.hideturtle()
turtle.done()